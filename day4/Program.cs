// operators
// variales of int
int a = 5;
int b = 10;
Console.WriteLine("for reference here are the number we use " + a + " and " + b); 

//perform basic arithmetic operations
int add = a + b;
int subtract = b-a;
int multiply = a*b;
int divide = b/a;

// Output the results
Console.WriteLine("Addition: " + add);
Console.WriteLine("Subtraction: " + subtract);
Console.WriteLine("Multiplication: " + multiply);
Console.WriteLine("Division: " + divide);

Console.WriteLine();

// variables of double
double da = 5.5;
double db = 10.5;
Console.WriteLine("for reference here are the number we use " + da + " and " + db); 
// perform basic arithmetic operations
double dadd = da + db;
double dsubtract = db - da;
double dmultiply = da * db;
double ddivide = db / da;

// Output the results
